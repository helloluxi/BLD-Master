        public static int[] skipAxis2 = new int[28];
        public static int[] skipAxis3 = new int[20];

        static Moves()
        {
            for (int i = 0; i < 29; i++)
            {
                std2move[Moves.move2std[i]] = i;
            }
            for (int i = 0; i < 21; i++)
            {
                std3move[Moves.move3std[i]] = i;
            }
            for (int i = 0; i < 36; i++)
            {
                for (int j = 0; j < 36; j++)
                {
                    ckmv[i, j] = (i / 3 == j / 3) || ((i / 3 % 3 == j / 3 % 3) && (i > j));
                }
                ckmv[36, i] = false;
            }
            for (int i = 0; i < 29; i++)
            {
                for (int j = 0; j < 28; j++)
                {
                    Moves.ckmv2[i, j] = ckmv[Moves.move2std[i], Moves.move2std[j]];
                }
            }
            for (int i = 0; i < 21; i++)
            {
                for (int j = 0; j < 20; j++)
                {
                    Moves.ckmv3[i, j] = ckmv[Moves.move3std[i], Moves.move3std[j]];
                }
            }
            for (int i = 0; i < 36; i++)
            {
                skipAxis[i] = 36;
                for (int j = i; j < 36; j++)
                {
                    if (!ckmv[i, j])
                    {
                        skipAxis[i] = j - 1;
                        break;
                    }
                }
            }
            for (int i = 0; i < 28; i++)
            {
                Moves.skipAxis2[i] = 28;
                for (int j = i; j < 28; j++)
                {
                    if (!Moves.ckmv2[i, j])
                    {
                        Moves.skipAxis2[i] = j - 1;
                        break;
                    }
                }
            }
            for (int i = 0; i < 20; i++)
            {
                Moves.skipAxis3[i] = 20;
                for (int j = i; j < 20; j++)
                {
                    if (!Moves.ckmv3[i, j])
                    {
                        Moves.skipAxis3[i] = j - 1;
                        break;
                    }
                }
            }
        }
